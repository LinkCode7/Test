int TestCodePoint::_testBF(std::shared_ptr<TestCodePointInfo> pContents)
{
	int count = 0;
	for (auto i = 0; i < pContents->m_codePoints.size();) {
		int step{ 1 };
		for (const auto& pItem : m_schema) {
			bool   match{ true };
			size_t start = pContents->m_codePoints[i].second.start, end{ 0 };

			auto emojiSize = pItem->m_codePoints.size();
			if (emojiSize > pContents->m_codePoints.size() - i)
				continue;

			for (auto j = 0; j < emojiSize; j++) {
				if (pContents->m_codePoints[i + j].first != pItem->m_codePoints[j]) {
					match = false;
					break;
				}
				end = pContents->m_codePoints[i + j].second.end;
			}

			if (match) {
				// ...
				++count;
				step = emojiSize;
				break;
			}
		}
		i += step;
	}

	return count;
}

