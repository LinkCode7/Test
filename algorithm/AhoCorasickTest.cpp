std::string TestCodePoint::schemaFind(SkUnichar code)
{
    for (const auto& item : m_schema)
    {
        for (const auto& codePoint : item->m_codePoints)
        {
            if (code == codePoint)
                return item->m_strUtf8;
        }
    }
    return "";
}

void TestCodePoint::initSchemaList()
{
    std::vector<std::string> arrPng;
    getEmojiList(arrPng);

    for (const auto& str : arrPng)
    {
        std::string strTemp{str};
        strTemp = strTemp.substr(0, strTemp.find("."));

        auto pInfo       = std::make_shared<TestSchemaCodePointInfo>();
        pInfo->m_strUtf8 = str;

        while (true)
        {
            int index = strTemp.find("-");
            if (index < 0)
            {
                pInfo->m_codePoints.push_back(hexStringToInt(strTemp));
                break;
            }
            std::string strSub = strTemp.substr(0, strTemp.find("-"));
            pInfo->m_codePoints.push_back(hexStringToInt(strSub));
            strTemp = strTemp.substr(strTemp.find("-") + 1);
        }
        m_schema.emplace_back(pInfo);
    }

    std::sort(m_schema.begin(), m_schema.end(),
              [](auto& left, auto& right) { return left->m_codePoints.size() > right->m_codePoints.size(); });
}

